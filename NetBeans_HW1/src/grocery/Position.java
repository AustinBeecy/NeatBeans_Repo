
package grocery;

/*
 * The Position class containing getters/setters
*/
public class Position {
    String empId; 
    String areaId; 
    
    
     public String getEmpID()
   {
     return this.empId;
   }
   public void setEmpID(String empId)
   {
       this.empId = empId;
   }
    public String getAreaID()
   {
     return this.areaId;
   }
   public void setAreaID(String areaId)
   {
       this.areaId = areaId; 
    }
}


